    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        int exists = 0; 
        for(Car car : cars)
        {
            if(car.getName() != null && car.getName().equals(jTextField1.getText()))
            {
             car.breaker();
             exists+=1;
                System.out.println(car.getName()+ car.isBroken());
                jTextField3.setText(car.getName()+ " " + car.isBroken());
            } 
        }
        if(exists ==0){
            System.out.println("There is no such car");
            jTextField3.setText("There is no such car");
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

