    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int exists = 0;  
        for(Car car : cars)
        {
            if(car.getName() != null && car.getName().equals(jTextField1.getText()))
            {
                exists+=1;
                
            } 
        }
        
        if(exists==0){
            cars.add( new Car(jTextField1.getText()));
             JOptionPane.showMessageDialog(null,"Машина" + " " + jTextField1.getText() + " " + "Добавлена","Вывод",JOptionPane.PLAIN_MESSAGE);
        }
        else{
            System.out.println("This car already exists");
            jTextField3.setText("This car already exists");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

